package FunctionLayer;

public class FKunde {

    String navn;
    String adresse;
    String by;
    String telefon;
    String email;


    public FKunde(String navn, String adresse, String by, String telefon, String email) {
        this.navn = navn;
        this.adresse = adresse;
        this.by = by;
        this.telefon = telefon;
        this.email = email;
    }
}
